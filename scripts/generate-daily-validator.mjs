import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import Ajv2020 from 'ajv/dist/2020.js';
import addFormats from 'ajv-formats';
import standaloneCode from 'ajv/dist/standalone/index.js';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const schemaPath = resolve(root, 'schemas/daily-report.schema.json');
const outputPath = resolve(root, 'src/daily/generated/dailyReportValidator.js');
const schema = JSON.parse(await readFile(schemaPath, 'utf8'));
const ajv = new Ajv2020({
    allErrors: true,
    strict: true,
    allowUnionTypes: true,
    code: { source: true, esm: true },
});
addFormats(ajv);
const generated = [
    '// Generated by scripts/generate-daily-validator.mjs. Do not edit manually.',
    standaloneCode(ajv, ajv.compile(schema)),
    '',
].join('\n');

if (process.argv.includes('--check')) {
    const current = await readFile(outputPath, 'utf8').catch(() => null);
    if (current !== generated) {
        console.error('Generated daily report validator is stale. Run npm run generate:validator.');
        process.exitCode = 1;
    } else {
        console.log('Generated daily report validator is current.');
    }
} else {
    await mkdir(dirname(outputPath), { recursive: true });
    await writeFile(outputPath, generated);
    console.log(`Generated ${outputPath}`);
}
